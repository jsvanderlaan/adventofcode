const score = {
    ')': 3,
    ']': 57,
    '}': 1197,
    '>': 25137,
}
const map = {
    '(': ')',
    '[': ']',
    '{': '}',
    '<': '>',
}

const getIllegalChar = (str) => {
    const closing = Object.keys(score)
    let check = ''
    for (let index = 0; index < str.length; index++) {
        const char = str[index]
        if (closing.includes(char)) {
            if (map[check.slice(-1)] !== char) {
                return char
            } else {
                check = check.slice(0, -1)
            }
        } else {
            check += char
        }
    }
    return null
}

console.log(
    input
        .map((x) => getIllegalChar(x))
        .filter((x) => x !== null)
        .reduce((p, n) => p + score[n], 0)
)

const score2 = {
    ')': 1,
    ']': 2,
    '}': 3,
    '>': 4,
}
const getCompletion = (str) => {
    const closing = Object.keys(score)
    let check = ''
    for (let index = 0; index < str.length; index++) {
        const char = str[index]
        if (closing.includes(char)) {
            if (map[check.slice(-1)] !== char) {
                return null
            } else {
                check = check.slice(0, -1)
            }
        } else {
            check += char
        }
    }
    let result = ''
    for (let index = check.length - 1; index >= 0; index--) {
        result += map[check[index]]
    }
    return result
}

const getMiddleScore = (arr) => {
    return arr.sort((a, b) => a - b)[Math.floor(arr.length / 2)]
}

console.log(
    getMiddleScore(
        input
            .map((x) => getCompletion(x))
            .filter((x) => x !== null)
            .map((x) => x.split('').reduce((p, n) => 5 * p + score2[n], 0))
    )
)
